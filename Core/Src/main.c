            // HAL_SPI_Receive(&hspi1, spi_flight_data, sizeof(spi_flight_data), HAL_MAX_DELAY);
            HAL_UART_Transmit(&huart2, "\r\n", strlen("\r\n"), 100);

            // char timeBuffer[20];
            // sprintf(timeBuffer, "Time: %lu ms\r\n", lastTransmitTime);

            // Transmit the string over UART
            // HAL_UART_Transmit(&huart2, (uint8_t*)timeBuffer, strlen(timeBuffer), HAL_MAX_DELAY);

